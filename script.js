function setLanguage(lang) {
    localStorage.setItem('preferredLang', lang);
    const langBtn = document.getElementById('current-lang');
    if (langBtn) langBtn.innerText = lang.toUpperCase();
    
    // Update Text Content
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerText = translations[lang][key];
        }
    });

    // Update Placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            el.setAttribute('placeholder', translations[lang][key]);
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    // Initialize Icons
    lucide.createIcons();
    
    // Initialize Language
    const savedLang = localStorage.getItem('preferredLang') || 'sq';
    setLanguage(savedLang);

    // 1. SCROLL REVEAL ANIMATION
    const revealElements = document.querySelectorAll('.reveal');
    
    const revealObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('active');
            }
        });
    }, { threshold: 0.1 });

    revealElements.forEach(el => revealObserver.observe(el));

    // 2. HEADER SCROLL EFFECT
    const headerEl = document.getElementById('header');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            headerEl.classList.add('scrolled');
        } else {
            headerEl.classList.remove('scrolled');
        }
    });

    // 3. COUNTDOWN LOGIC
    const countdownDate = new Date("Sep 1, 2027 00:00:00").getTime();

    const updateCountdown = () => {
        const now = new Date().getTime();
        const distance = countdownDate - now;

        const days = Math.floor(distance / (1000 * 60 * 60 * 24));
        const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
        const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));

        const daysEl = document.getElementById("days");
        const hoursEl = document.getElementById("hours");
        const minutesEl = document.getElementById("minutes");

        if(daysEl) daysEl.innerText = days.toString().padStart(2, '0');
        if(hoursEl) hoursEl.innerText = hours.toString().padStart(2, '0');
        if(minutesEl) minutesEl.innerText = minutes.toString().padStart(2, '0');

        if (distance < 0) {
            clearInterval(timerInterval);
            const countdownEl = document.getElementById("countdown");
            if(countdownEl) countdownEl.innerHTML = "PROJEKTI KA FILLUAR!";
        }
    };

    const timerInterval = setInterval(updateCountdown, 1000);
    updateCountdown(); 

    // 4. SMOOTH SCROLL
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                window.scrollTo({
                    top: target.offsetTop - 80,
                    behavior: 'smooth'
                });
            }
        });
    });

    // 5. FORM SUBMISSION
    const forms = document.querySelectorAll('form');
    forms.forEach(form => {
        form.addEventListener('submit', (e) => {
            e.preventDefault();
            const btn = form.querySelector('button');
            const originalText = btn.innerText;
            
            btn.innerText = "...";
            btn.disabled = true;

            setTimeout(() => {
                alert("Sucesso!");
                btn.innerText = originalText;
                btn.disabled = false;
                form.reset();
                // Reset custom select
                const triggerSpan = document.querySelector('.select-trigger span');
                if(triggerSpan) triggerSpan.setAttribute('data-i18n', 'form_city_select');
                setLanguage(localStorage.getItem('preferredLang') || 'sq');
            }, 1500);
        });
    });



    // === LANGUAGE SELECTOR ===
    const customSelect = document.getElementById('city-select');
    if (customSelect) {
        const trigger = customSelect.querySelector('.select-trigger');
        const options = customSelect.querySelectorAll('.option');
        const hiddenInput = document.getElementById('selected-city');
        const triggerText = trigger.querySelector('span');

        trigger.addEventListener('click', (e) => {
            e.stopPropagation();
            customSelect.classList.toggle('open');
        });

        options.forEach(opt => {
            opt.addEventListener('click', () => {
                const value = opt.getAttribute('data-value');
                const i18nKey = opt.getAttribute('data-i18n');
                const text = opt.innerText;
                
                triggerText.innerText = text;
                triggerText.setAttribute('data-i18n', i18nKey); // Update i18n key for trigger
                hiddenInput.value = value;
                customSelect.classList.remove('open');
            });
        });

        document.addEventListener('click', () => {
            customSelect.classList.remove('open');
        });
    }

    // === GLOBO PREMIUM ANIMATION SYSTEM ===
    const canvas = document.getElementById('globe-canvas');
    if (canvas) {
        const context = canvas.getContext('2d', { alpha: true });
        const frameCount = 85;
        const images = [];
        const globeObj = { frame: 1 };
        const placeholder = document.getElementById('logo-globe-container');
        const globeScene = document.getElementById('globe-scene');

        // Preload frames in batches
        const preloadImages = async () => {
            const firstBatch = 15;
            // Load first batch for immediate interaction
            for (let i = 1; i <= firstBatch; i++) {
                await loadSingleImage(i);
            }
            initGlobeAnimation();
            
            // Load the rest in background with slight delay to prevent jank
            for (let i = firstBatch + 1; i <= frameCount; i++) {
                setTimeout(() => {
                    loadSingleImage(i);
                }, (i - firstBatch) * 50); // 50ms entre cada frame para suavizar o I/O
            }
        };

        const loadSingleImage = (i) => {
            return new Promise(resolve => {
                const img = new Image();
                img.src = `frames/frame_${i.toString().padStart(4, '0')}.webp`;
                img.onload = () => {
                    images[i - 1] = img;
                    if (i === 1) render();
                    resolve();
                };
                img.onerror = () => {
                    console.error(`Failed to load frame ${i}`);
                    resolve();
                };
            });
        };

        let lastFrame = -1;
        const render = () => {
            const index = Math.min(Math.max(Math.round(globeObj.frame) - 1, 0), frameCount - 1);
            if (index === lastFrame) return; // Evita redesenho se o frame for o mesmo
            
            const img = images[index];
            if (img && img.complete) {
                context.clearRect(0, 0, canvas.width, canvas.height);
                context.drawImage(img, 0, 0, canvas.width, canvas.height);
                lastFrame = index;
            }
        };

        const initGlobeAnimation = () => {
            canvas.width = 800;
            canvas.height = 800;
            context.imageSmoothingEnabled = true;
            context.imageSmoothingQuality = 'low'; // Ganho de performance mantendo a qualidade visual do WebP
            
            // Lenis with optimized settings
            const lenis = new Lenis({
                lerp: 0.2, // Mais direto, menos "pesado"
                smoothWheel: true,
                wheelMultiplier: 0.9, // Reduzido levemente para evitar saltos bruscos
                touchMultiplier: 1.5
            });
            
            lenis.on('scroll', ScrollTrigger.update);
            gsap.ticker.add((time) => {
                lenis.raf(time * 1000);
            });
            // Performance Config
            gsap.ticker.fps(60);
            gsap.ticker.lagSmoothing(0); // Garante que a animação não pule frames no scroll

            // Estado inicial: Menor para evitar pixelização, no canto superior esquerdo
            gsap.set(canvas, {
                scale: 1.1, // Quase o tamanho real do frame
                x: -window.innerWidth * 0.4,
                y: -window.innerHeight * 0.25,
                filter: "brightness(0.3) blur(1px)"
            });

            const tl = gsap.timeline({
                scrollTrigger: {
                    trigger: "body",
                    start: "top top",
                    end: "60% top",
                    scrub: 0.5, // Resposta quase instantânea ao scroll
                    invalidateOnRefresh: true
                }
            });

            // Transição para o centro e brilho total
            tl.to(canvas, {
                scale: window.innerWidth < 768 ? 0.4 : 0.65, // Bem menor para ficar nítido
                x: 0,
                y: 0,
                filter: "brightness(1) blur(0px)",
                ease: "power2.inOut"
            }, 0);

            // Progressão dos frames
            tl.to(globeObj, {
                frame: frameCount,
                snap: "frame",
                ease: "none",
                onUpdate: render
            }, 0);

            window.addEventListener('resize', () => ScrollTrigger.refresh());
            
            render();
        };

        preloadImages();
    }

});
